#!/bin/bash

echo "🧪 Тестирование автоматического создания администратора"
echo "=========================================================="

# Удаляем всех админов из БД
echo "🗑️  Удаление существующих администраторов..."
docker-compose exec -T db psql -U ${POSTGRES_USER:-postgres} -d ${POSTGRES_DB:-ai_landing_db} -c "DELETE FROM users WHERE role = 'admin';"

if [ $? -eq 0 ]; then
    echo "✅ Администраторы удалены"
else
    echo "❌ Ошибка при удалении администраторов"
    exit 1
fi

# Перезапускаем контейнер FastAPI
echo ""
echo "🔄 Перезапуск контейнера FastAPI..."
docker-compose restart web

echo ""
echo "⏳ Ожидание запуска приложения (10 секунд)..."
sleep 10

echo ""
echo "📋 Логи создания администратора:"
echo "=========================================================="
docker-compose logs web | grep -A 10 "НОВЫЙ АДМИНИСТРАТОР"

echo ""
echo "=========================================================="
echo "✅ Тест завершен!"
echo "Проверьте логи выше для получения данных администратора"
